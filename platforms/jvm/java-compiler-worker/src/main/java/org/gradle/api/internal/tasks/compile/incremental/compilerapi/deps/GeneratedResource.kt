package org.gradle.api.internal.tasks.compile.incremental.compilerapi.deps

import com.google.common.base.Objects
import java.io.Serializable
import javax.tools.JavaFileManager
import javax.tools.StandardLocation

/**
 * Uniquely identifies a resource that was generated by an annotation processor.
 */
class GeneratedResource(@JvmField val location: Location?, @JvmField val path: String) : Serializable {
    /**
     * The supported locations into which generated resources may be placed.
     */
    enum class Location {
        CLASS_OUTPUT,
        SOURCE_OUTPUT,
        NATIVE_HEADER_OUTPUT,
        ;

        companion object {
            /**
             * @return null if the given location is not supported.
             */
            fun from(location: JavaFileManager.Location?): Location? {
                if (location is StandardLocation) {
                    when (location) {
                        StandardLocation.CLASS_OUTPUT -> return Location.CLASS_OUTPUT
                        StandardLocation.SOURCE_OUTPUT -> return Location.SOURCE_OUTPUT
                        StandardLocation.NATIVE_HEADER_OUTPUT -> return Location.NATIVE_HEADER_OUTPUT
                        else -> {}
                    }
                }
                return null
            }
        }
    }

    constructor(location: Location?, pkg: CharSequence, relativeName: CharSequence) : this(
        location,
        if (pkg.length == 0) relativeName.toString() else pkg.toString().replace('.', '/') + '/' + relativeName
    )

    override fun equals(o: Any?): Boolean {
        if (this === o) {
            return true
        }
        if (o == null || javaClass != o.javaClass) {
            return false
        }
        val that = o as GeneratedResource
        return location == that.location &&
                path == that.path
    }

    override fun hashCode(): Int {
        return Objects.hashCode(location, path)
    }

    override fun toString(): String {
        return path + " in " + location
    }
}
